    private void SNBuchLeihenButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SNBuchLeihenButtonActionPerformed
        
        if(!studentnummerFeld.getText().isEmpty()){
            BuchLeihenColumnsMitID buch_leihen_objekt;
            Bibliothek bibliothek_object = new Bibliothek();
            
            /*
            //int buchID = Integer.valueOf(bibliothek_object.BBuchIDfeld.getText());
            String namevonBuch = bibliothek_object.BNamevonBuchsFeld.getText();
            String author = bibliothek_object.BAutorsFeld.getText();
            String thema = bibliothek_object.BThemaFeld.getText();
            String verlag = bibliothek_object.BVerlagsFeld.getText();
            */
            String studentnummer = studentnummerFeld.getText();
            String datum = datumFeld.getText();
            
            buch_leihen_objekt = new BuchLeihenColumnsMitID(Integer.valueOf(buchLeihenColumns_objekt.getBuchID()),buchLeihenColumns_objekt.getBuchname(),buchLeihenColumns_objekt.getAuthor(),buchLeihenColumns_objekt.getAuflagesdatum(),
                            buchLeihenColumns_objekt.getThema(),buchLeihenColumns_objekt.getVerlag(),studentnummer,datum);
            buch_leihen_bücherlist.add(buch_leihen_objekt);

            PreparedStatement st;
            ResultSet rs;
            
            String SQLAnfrage = "INSERT INTO `buchleihen` (`buchID`,`buchname`,`author`,`auflagesdatum`,`thema`,`verlag`,`studentnummer`,`datum`) VALUES(?,?,?,?,?,?,?,?)";

            try {
                st = dBVerbindung.getConnection().prepareStatement(SQLAnfrage);
                
                st.setInt(1, buch_leihen_bücherlist.get(0).getBuchID());
                st.setString(2, buch_leihen_bücherlist.get(0).getBuchname());
                st.setString(3, buch_leihen_bücherlist.get(0).getAuthor());
                st.setString(4, buch_leihen_bücherlist.get(0).getAuflagesdatum());
                st.setString(5, buch_leihen_bücherlist.get(0).getThema());
                st.setString(6, buch_leihen_bücherlist.get(0).getVerlag());
                st.setString(7, buch_leihen_bücherlist.get(0).getStudentnummer());
                st.setString(8, buch_leihen_bücherlist.get(0).getDatum());
                
                //prüfen ob daten erfolgreich in System gespeichert werden
                if(st.executeUpdate()!=0){
                    JOptionPane.showMessageDialog(null, "Buch wurde dem Student geliehen.");
                    dispose();
                    //bücherdaten_live_zeigen();
                    //FelderSauberMachen();
                }
                
                new Bibliothek().setVisible(true);
            } catch (Exception e) {
                System.out.println(e);
            }

        }else{
            JOptionPane.showMessageDialog(null, "Jedes Feld muss vollständig erfüllt sein!","FEHLER",2);
        }
        
    }//GEN-LAST:event_SNBuchLeihenButtonActionPerformed

