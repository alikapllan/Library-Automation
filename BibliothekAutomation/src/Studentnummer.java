    private void studentnummerFeldKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_studentnummerFeldKeyTyped
        //Hier ermöglicht uns, dass man für Studentnummer maximal 10 Karakter benutzen darf.
        if((studentnummerFeld.getText() + evt.getKeyChar()).length() >= 10){
            evt.consume();
        }
        
        //Hier erlauben wir den User, dass er nur Nummer als Input eingibt
        if(!Character.isDigit(evt.getKeyChar())){
            evt.consume();
        }
    }//GEN-LAST:event_studentnummerFeldKeyTyped

